print(f"The Multiplication of {a} and {b} is a*b : {a*b}")
print(f"The Floor division of {a} and {b} is a//b : {a//b}")
print(f"The modular division of {a} and {b} is a%b: {a%b}")
print(f"The Exponent  of {a} and {b} is a**b: {a**b}")