'''In a programming the type conversion is the process of converting the data of one data type to another data type 
   There are Two type of type conversions 
   1. Implicit Type conversions:- Python automatically converts the data from one data type to another data type , There won't be any data loss in this type.
   2. Explicit type conversions :- User converts the data type of any object to the required data type , There can be loss of data in this type of type conversion.
   '''

a=100
b=1.6
print(type(a))
print(type(b))
c=float(a)
d=int(b)
print(type(c))
print(type(d))
print(c)
print(d)